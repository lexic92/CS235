package cs235.xml;

public class Attribute {
	private String name;
	private String value;
	
	Attribute(String name, String value){
		this.setName(name);
		this.setValue(value);
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getValue() {
		return value;
	}
}
